num1= int(input("Ingresa el primer numero:"))
num1 = int(num1)

num2 = int(input("Ingresa el segundo numero:"))


if (num1 > num2):
    print(str(num1) + "\nEs mayor que " + str(num2))

elif num1 == num2:
    print(str(num1) + "\nEs igual que " + str(num2))

else:
    print(str(num1) + "\nEs menor que " + str(num2))

